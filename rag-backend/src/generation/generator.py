    def generate_grouped_answer(
        self, 
        plan: Dict[str, Any], 
        chunks: List[Dict[str, Any]]
    ) -> str:
        """
        Generate grouped answer organized by sub-questions (Stage 3).
        
        Takes the decomposed plan from PlannerAgent and retrieved chunks,
        then organizes the output by sub-question with claims and evidence.
        
        Args:
            plan: Dictionary with main_question, sub_questions, search_queries
            chunks: Retrieved chunks from multi_retrieve
            
        Returns:
            Formatted answer grouped by sub-question
        """
        main_question = plan.get("main_question", "")
        sub_questions = plan.get("sub_questions", [])
        resolved_user_level = (plan.get("resolved_user_level") or "auto").lower()
        subquestion_intents = plan.get("subquestion_intents") or {}
        
        if not chunks:
            return "I could not find relevant information to answer your question."
        
        output = f"\n{'='*80}\n"
        output += f"AGENTIC RAG RESPONSE\n"
        output += f"{'='*80}\n\n"
        output += f"📝 Main Question: {main_question}\n\n"

        analysis_data: Dict[str, Any] = {
            "query": main_question,
            "generated_at": "",
            "sub_questions": [],
            "references": [],
            "final_summary": "",
            "stats": {},
        }
        
        # Coverage-first assignment (soft scoring, no hard filtering)
        try:
            chunk_assignments = self._assign_chunks_to_subquestions(
                sub_questions,
                chunks,
                subquestion_intents=subquestion_intents,
            )
        except TypeError:
            chunk_assignments = self._assign_chunks_to_subquestions(sub_questions, chunks)

        # Adaptive retry (generation-side): broaden assignment if usable evidence is weak.
        min_total_target = max(len(sub_questions) * self.MIN_CHUNKS_PER_SUBQ, self.MIN_CHUNKS_PER_SUBQ)
        usable_now = sum(
            1
            for sq in sub_questions
            for c in chunk_assignments.get(sq, [])
            if float(c.get("subquery_similarity", 0.0) or 0.0) >= 0.50
        )
        if usable_now < min_total_target:
            try:
                retry_assignments = self._assign_chunks_to_subquestions(
                    sub_questions,
                    chunks,
                    adaptive_round=1,
                    subquestion_intents=subquestion_intents,
                )
            except TypeError:
                # Backward compatibility for monkeypatched/stubbed callables that
                # still expose the legacy 2-arg signature.
                retry_assignments = self._assign_chunks_to_subquestions(sub_questions, chunks)
            for sq in sub_questions:
                merged = chunk_assignments.get(sq, []) + retry_assignments.get(sq, [])
                dedup: Dict[str, Dict[str, Any]] = {}
                for c in merged:
                    cid = str(c.get("chunk_id", ""))
                    if not cid:
                        continue
                    if cid not in dedup or float(c.get("final_score", 0.0) or 0.0) > float(dedup[cid].get("final_score", 0.0) or 0.0):
                        dedup[cid] = c
                chunk_assignments[sq] = sorted(
                    dedup.values(),
                    key=lambda x: float(x.get("final_score", 0.0) or 0.0),
                    reverse=True,
                )

        evidence_extractor = None
        try:
            from src.evidence.extractor import EvidenceExtractor
            evidence_extractor = EvidenceExtractor()
        except Exception:
            evidence_extractor = None
        
        global_top_chunks = sorted(
            chunks,
            key=lambda c: float(
                c.get("rrf_score", c.get("final_score", c.get("hybrid_score", c.get("similarity_score", 0.0))))
                or 0.0
            ),
            reverse=True,
        )

        embedder = None
        chunk_embeddings: List[Any] = []
        try:
            from src.embeddings.embedder import get_shared_embedder
            embedder = get_shared_embedder()
            chunk_embeddings = [embedder.embed_text(c.get("text", "")) for c in chunks]
        except Exception:
            embedder = None
            chunk_embeddings = []

        # Generate answer for each sub-question
        for i, sub_q in enumerate(sub_questions, 1):
            output += f"🔹 Sub-question: {sub_q}\n\n"
            subq_intent = self._infer_intent(sub_q, subquestion_intents.get(sub_q, ""))
            subq_data: Dict[str, Any] = {
                "question": sub_q,
                "intent": subq_intent,
                "debug": {},
                "papers": [],
                "conflicts": [],
                "comparison_text": "",
                "mini_summary": "",
            }
            
            assigned_chunks = chunk_assignments.get(sub_q, [])

            # Deduplicate per sub-question by chunk_id (avoid duplication explosion).
            dedup_assigned: Dict[str, Dict[str, Any]] = {}
            for chunk in assigned_chunks:
                cid = str(chunk.get("chunk_id", ""))
                if not cid:
                    continue
                score = float(chunk.get("subquery_similarity", 0.0) or 0.0)
                if cid not in dedup_assigned or score > float(dedup_assigned[cid].get("subquery_similarity", 0.0) or 0.0):
                    dedup_assigned[cid] = chunk
            assigned_chunks = list(dedup_assigned.values())

            dataset_subq = self._is_dataset_subquestion(sub_q)
            rescored_chunks: List[Dict[str, Any]] = []
            for chunk in assigned_chunks:
                text = chunk.get("text", "")
                if not text:
                    continue

                sq_score = float(chunk.get("subquery_similarity", 0.0) or 0.0)
                evidence_score = float(chunk.get("evidence_score", 0.0) or 0.0)
                rrf = float(chunk.get("rrf_score", 0.0) or 0.0)
                kw_overlap = self._keyword_overlap(sub_q, text)

                penalty = 0.0
                if sq_score < 0.60:
                    penalty += (0.60 - sq_score) * 0.35
                if self._is_background_drift(sub_q, text, resolved_user_level):
                    penalty += 0.15

                if dataset_subq:
                    ds_signal = self._dataset_signal_score(text)
                    if ds_signal <= 0.0:
                        penalty += 0.25
                    else:
                        sq_score = min(1.0, sq_score + 0.10 * ds_signal)

                final_score = (
                    (0.4 * rrf)
                    + (0.3 * sq_score)
                    + (0.2 * evidence_score)
                    + (0.1 * kw_overlap)
                    - penalty
                )

                rescored_chunks.append({
                    **chunk,
                    "subquery_similarity": sq_score,
                    "keyword_overlap": kw_overlap,
                    "penalty": penalty,
                    "final_score": final_score,
                    "sub_question": sub_q,
                })

            rescored_chunks.sort(key=lambda c: float(c.get("final_score", 0.0) or 0.0), reverse=True)

            # Coverage guarantee with multi-stage fallback.
            filtered_chunks = list(rescored_chunks[: self.MAX_CHUNKS_PER_SUBQ])
            fallback_triggered = False
            fallback_stages_used: List[str] = []

            if len(filtered_chunks) < self.MIN_CHUNKS_PER_SUBQ:
                fallback_triggered = True

                # Stage 1: Include lower-ranked RRF results
                stage1 = sorted(
                    rescored_chunks,
                    key=lambda x: float(x.get("rrf_score", 0.0) or 0.0),
                    reverse=True,
                )
                if stage1:
                    fallback_stages_used.append("stage1_lower_ranked_rrf")
                for c in stage1:
                    if len(filtered_chunks) >= self.MIN_CHUNKS_PER_SUBQ:
                        break
                    if c not in filtered_chunks:
                        filtered_chunks.append(c)

                # Stage 2: Relax similarity thresholds
                stage2 = [
                    c for c in rescored_chunks
                    if float(c.get("subquery_similarity", 0.0) or 0.0) >= 0.35
                ]
                if stage2:
                    fallback_stages_used.append("stage2_relaxed_similarity")
                for c in stage2:
                    if len(filtered_chunks) >= self.MIN_CHUNKS_PER_SUBQ:
                        break
                    if c not in filtered_chunks:
                        filtered_chunks.append(c)

                # Stage 3: Use global chunk pool
                if len(filtered_chunks) < self.MIN_CHUNKS_PER_SUBQ:
                    fallback_stages_used.append("stage3_global_pool")
                    global_candidates = []
                    for gc in global_top_chunks:
                        text = gc.get("text", "")
                        if not text:
                            continue
                        aligned = self._chunk_matches_intent(text, subq_intent)
                        if not aligned and subq_intent == "methodology":
                            aligned = self._lexical_similarity(sub_q, text) >= 0.03
                        if not aligned:
                            continue
                        sq_score = max(
                            self._lexical_similarity(sub_q, text),
                            float(gc.get("similarity_score", 0.0) or 0.0) * 0.7,
                        )
                        kw_overlap = self._keyword_overlap(sub_q, text)
                        score = (
                            (0.4 * float(gc.get("rrf_score", 0.0) or 0.0))
                            + (0.3 * sq_score)
                            + (0.2 * float(gc.get("evidence_score", 0.0) or 0.0))
                            + (0.1 * kw_overlap)
                        )
                        global_candidates.append({
                            **gc,
                            "subquery_similarity": sq_score,
                            "keyword_overlap": kw_overlap,
                            "final_score": score,
                            "sub_question": sub_q,
                        })
                    global_candidates.sort(key=lambda x: float(x.get("final_score", 0.0) or 0.0), reverse=True)
                    for c in global_candidates:
                        if len(filtered_chunks) >= self.MIN_CHUNKS_PER_SUBQ:
                            break
                        if c not in filtered_chunks:
                            filtered_chunks.append(c)

                # Stage 4: Include abstract-based chunks
                if len(filtered_chunks) < self.MIN_CHUNKS_PER_SUBQ:
                    fallback_stages_used.append("stage4_abstract_chunks")
                    abstract_chunks = [
                        c for c in chunks
                        if str((c.get("metadata") or {}).get("section", c.get("section", ""))).lower() == "abstract"
                    ]
                    abstract_chunks.sort(
                        key=lambda x: float(x.get("rrf_score", x.get("similarity_score", 0.0)) or 0.0),
                        reverse=True,
                    )
                    for c in abstract_chunks:
                        if len(filtered_chunks) >= self.MIN_CHUNKS_PER_SUBQ:
                            break
                        aligned = self._chunk_matches_intent(c.get("text", ""), subq_intent)
                        if not aligned and subq_intent == "methodology":
                            aligned = self._lexical_similarity(sub_q, c.get("text", "")) >= 0.03
                        if not aligned:
                            continue
                        if c not in filtered_chunks:
                            filtered_chunks.append({
                                **c,
                                "subquery_similarity": max(
                                    self._lexical_similarity(sub_q, c.get("text", "")),
                                    float(c.get("similarity_score", 0.0) or 0.0) * 0.7,
                                ),
                                "keyword_overlap": self._keyword_overlap(sub_q, c.get("text", "")),
                                "final_score": float(c.get("rrf_score", c.get("similarity_score", 0.0)) or 0.0),
                                "sub_question": sub_q,
                            })

            # Deduplicate by chunk_id and keep strongest score
            dedup_filtered: Dict[str, Dict[str, Any]] = {}
            for c in filtered_chunks:
                cid = str(c.get("chunk_id", ""))
                if not cid:
                    continue
                if cid not in dedup_filtered or float(c.get("final_score", 0.0) or 0.0) > float(dedup_filtered[cid].get("final_score", 0.0) or 0.0):
                    dedup_filtered[cid] = c
            filtered_chunks = sorted(
                dedup_filtered.values(),
                key=lambda c: float(c.get("final_score", 0.0) or 0.0),
                reverse=True,
            )[: max(self.MAX_CHUNKS_PER_SUBQ, self.MIN_CHUNKS_PER_SUBQ)]

            # Paper coverage guarantee: ensure at least MIN_PAPERS_PER_SUBQ papers.
            paper_ids_now = {
                str(c.get("paper_id", "")) for c in filtered_chunks if str(c.get("paper_id", ""))
            }
            if len(paper_ids_now) < self.MIN_PAPERS_PER_SUBQ:
                fallback_triggered = True
                if "paper_coverage_boost" not in fallback_stages_used:
                    fallback_stages_used.append("paper_coverage_boost")

                paper_candidates = []
                for gc in global_top_chunks:
                    pid = str(gc.get("paper_id", ""))
                    if not pid or pid in paper_ids_now:
                        continue
                    text = gc.get("text", "")
                    aligned = self._chunk_matches_intent(text, subq_intent)
                    if not aligned and subq_intent == "methodology":
                        aligned = self._lexical_similarity(sub_q, text) >= 0.03
                    if not aligned:
                        continue
                    sq_score = max(
                        self._lexical_similarity(sub_q, text),
                        float(gc.get("similarity_score", 0.0) or 0.0) * 0.7,
                    )
                    kw_overlap = self._keyword_overlap(sub_q, text)
                    score = (
                        (0.4 * float(gc.get("rrf_score", 0.0) or 0.0))
                        + (0.3 * sq_score)
                        + (0.2 * float(gc.get("evidence_score", 0.0) or 0.0))
                        + (0.1 * kw_overlap)
                    )
                    paper_candidates.append({
                        **gc,
                        "subquery_similarity": sq_score,
                        "keyword_overlap": kw_overlap,
                        "final_score": score,
                        "sub_question": sub_q,
                    })

                paper_candidates.sort(key=lambda x: float(x.get("final_score", 0.0) or 0.0), reverse=True)
                for c in paper_candidates:
                    if len(paper_ids_now) >= self.MIN_PAPERS_PER_SUBQ:
                        break
                    cid = str(c.get("chunk_id", ""))
                    if not cid:
                        continue
                    if cid not in {str(fc.get("chunk_id", "")) for fc in filtered_chunks}:
                        filtered_chunks.append(c)
                        pid = str(c.get("paper_id", ""))
                        if pid:
                            paper_ids_now.add(pid)

            filtered_chunks = sorted(
                filtered_chunks,
                key=lambda c: float(c.get("final_score", 0.0) or 0.0),
                reverse=True,
            )[: max(self.MAX_CHUNKS_PER_SUBQ + 2, self.MIN_CHUNKS_PER_SUBQ)]

            if filtered_chunks:
                output += f"Intent focus: {subq_intent} | User level: {resolved_user_level}\n\n"

            print(
                f"SubQ: {sub_q} → assigned: {len(assigned_chunks)} → rescored: {len(rescored_chunks)} → after filter: {len(filtered_chunks)} "
                f"→ fallback: {fallback_triggered} → papers: {len({str(c.get('paper_id','')) for c in filtered_chunks if str(c.get('paper_id',''))})}"
            )
            papers_used = len({str(c.get("paper_id", "")) for c in filtered_chunks if str(c.get("paper_id", ""))})
            subq_data["debug"] = {
                "assigned": len(assigned_chunks),
                "rescored": len(rescored_chunks),
                "after_filter": len(filtered_chunks),
                "fallback": fallback_triggered,
                "fallback_used": fallback_triggered,
                "fallback_stages": fallback_stages_used,
                "fallback_triggered": fallback_triggered,
                "num_papers_used": papers_used,
                "intent": subq_intent,
                "user_level": resolved_user_level,
            }
            
            if not filtered_chunks:
                output += "  ⚠ No specific evidence found for this sub-question.\n\n"
                subq_data["mini_summary"] = "No sufficiently relevant evidence was retained for this sub-question."
                analysis_data["sub_questions"].append(subq_data)
                continue

            output += "📌 Evidence Units (Grouped by Paper)\n\n"
            output += "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n"

            units: List[Dict[str, Any]] = []
            for chunk in filtered_chunks[:8]:
                subquery_similarity = float(
                    chunk.get(
                        "subquery_similarity",
                        chunk.get("query_similarity", chunk.get("similarity_score", 0.0)),
                    )
                    or 0.0
                )
                unit = self._build_evidence_unit(
                    sub_q,
                    chunk,
                    evidence_extractor,
                    subquery_similarity,
                )
                if unit:
                    units.append(unit)

            if not units:
                output += "⚠ No sufficiently relevant evidence found for this sub-question.\n\n"
                output += "⚠️ Cross-Paper Conflict Analysis\n"
                output += "No conflict analysis available due to missing evidence.\n\n"
                output += "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n"
                output += "📊 Comparison Summary\n"
                output += "- Supporting evidence clusters: 0\n"
                output += "- Conflicting clusters: 0\n"
                output += "- Consensus level: Low\n\n"
                subq_data["mini_summary"] = "No sufficiently relevant evidence was retained after evidence-unit construction."
                analysis_data["sub_questions"].append(subq_data)
                continue

            # Deduplicate units by chunk-level claim signature
            dedup_map: Dict[str, Dict[str, Any]] = {}
            for u in units:
                key = f"{u.get('paper_id','')}::{u.get('section','')}::{u.get('claim','')[:100]}"
                if key not in dedup_map or u.get("relevance", 0) > dedup_map[key].get("relevance", 0):
                    dedup_map[key] = u
            units = list(dedup_map.values())

            # STEP 6: Consolidate evidence by paper
            merged_evidence = self._merge_paper_evidence(units)
            paper_profiles = self._build_paper_profiles(filtered_chunks)
            
            # STEP 8: Quality gate before output
            quality_check = self._quality_gate(merged_evidence)
            if quality_check["status"] == "rejected":
                output += f"⚠️ Quality Gate REJECTED: {quality_check.get('issues', ['Unknown issue'])}\n\n"
                subq_data["mini_summary"] = f"Evidence rejected by quality gate: {quality_check.get('issues', ['Unknown issue'])}"
                analysis_data["sub_questions"].append(subq_data)
                continue
            
            if quality_check.get("gaps"):
                for gap in quality_check["gaps"]:
                    output += f"⚠️ Coverage Gap: {gap}\n"
                output += "\n"

            grouped: Dict[str, Dict[str, List[Dict[str, Any]]]] = {}
            for unit in units:
                key = f"{unit['paper_title']} ({unit['paper_year']})"
                section_key = unit.get("section", "unknown")
                grouped.setdefault(key, {})
                grouped[key].setdefault(section_key, []).append(unit)

            for paper_key, section_groups in grouped.items():
                output += f"📄 Paper: {paper_key}\n\n"
                idx = 1
                paper_units: List[Dict[str, Any]] = []
                preferred_sections = self._section_preferences(sub_q)
                ordered_sections = sorted(
                    section_groups.items(),
                    key=lambda kv: (
                        1 if kv[0] in preferred_sections else 0,
                        max(
                            (
                                ((u.get("confidence", 0) * 0.6) + (u.get("relevance", 0) * 0.4))
                                * (self.SECTION_PREFERRED_BOOST if kv[0] in preferred_sections else 1.0)
                            )
                            for u in kv[1]
                        ) if kv[1] else 0,
                    ),
                    reverse=True,
                )[: self.MAX_SECTIONS_PER_PAPER]

                for section_name, section_units in ordered_sections:
                    sorted_units = sorted(
                        section_units,
                        key=lambda u: (u.get("final_score", 0), u.get("confidence", 0), u.get("relevance", 0)),
                        reverse=True,
                    )[: self.TOP_CHUNKS_PER_SECTION]

                    for unit in sorted_units:
                        paper_units.append(unit)
                        output += f"[{idx}] Section: {section_name}\n"
                        output += f"Location: sentences {unit['location_start']}–{unit['location_end']}\n"
                        
                        # Add traceability metadata with section correction flag
                        if unit.get("section_corrected"):
                            output += f"[⚠️ Section auto-corrected from: {unit.get('labeled_section', 'unknown')}]\n"
                        if unit.get("nearest_heading"):
                            output += f"[Heading: {unit.get('nearest_heading')}]\n"
                        
                        output += (
                            f"Relevance: {unit['relevance']:.2f} | "
                            f"SubQ Similarity: {unit['subquery_similarity']:.2f} | "
                            f"Confidence: {unit['confidence']:.2f} ({unit['confidence_band']})\n\n"
                        )
                        output += "Text:\n"
                        output += f"\"{unit['text']}\"\n\n"
                        output += "---\n\n"
                        idx += 1

                first_unit = paper_units[0] if paper_units else {}
                subq_data["papers"].append({
                    "paper_id": first_unit.get("paper_id", ""),
                    "paper_title": first_unit.get("paper_title", paper_key),
                    "paper_year": first_unit.get("paper_year", "N/A"),
                    "doi": first_unit.get("paper_doi", ""),
                    "link": first_unit.get("paper_link", ""),
                    "evidence_units": paper_units,
                })
                output += "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n"
            
            # STEP 7: Output structured evidence blocks
            output += "## Evidence by Paper (Merged, Structured)\n\n"
            for paper_id, paper_data in merged_evidence.items():
                output += f"### {paper_data['paper_title']} ({paper_data['paper_year']})\n"
                output += f"**Sections represented:** {', '.join(paper_data.get('sections', []))}\n\n"
                
                if paper_data.get("traceable_citations"):
                    output += f"**Key evidence points:**\n"
                    for i, citation in enumerate(paper_data["traceable_citations"][:3], 1):
                        output += f"\n{i}. **Section:** {citation['section']}\n"
                        if citation.get("heading"):
                            output += f"   **Heading:** {citation['heading']}\n"
                        output += f"   **Location:** {citation['location']}\n"
                        output += f"   **Evidence:** \"{citation['claim'][:150]}...\"\n"
                output += "\n"
            
            # Output traceable citations
            output += "## Traceable Citations\n\n"
            for paper_id, paper_data in merged_evidence.items():
                for citation in paper_data["traceable_citations"]:
                    output += f"[{paper_data['paper_title']} | {citation['section']} | {citation.get('heading', 'N/A')} | {citation['location']}]\n"
                    output += f"\"{citation['text']}\"\n\n"
            
            # Output missing evidence / retrieval gaps if any
            if quality_check.get("gaps"):
                output += "## Missing Evidence / Retrieval Gaps\n\n"
                for gap in quality_check["gaps"]:
                    output += f"- {gap}\n"
                output += "\n"

            conflicts = ConflictDetector.detect_conflicts(units)
            output += "⚠️ Cross-Paper Conflict Analysis\n\n"
            unique_papers = {u.get("paper_id") for u in units if u.get("paper_id")}
            if len(unique_papers) < 2:
                output += "Only one paper available; cross-paper conflict analysis is not applicable.\n\n"
            elif conflicts:
                first = conflicts[0]
                a = first["a"]
                b = first["b"]
                subq_data["conflicts"] = [
                    {
                        "claim_a": (c.get("a", {}).get("claim") or "")[:220],
                        "claim_b": (c.get("b", {}).get("claim") or "")[:220],
                        "type": c.get("type", "Conceptual"),
                        "strength": float(c.get("strength", 0.0) or 0.0),
                        "explanation": c.get("explanation", ""),
                    }
                    for c in conflicts[:3]
                ]
                output += (
                    f"* Claim A ({a['paper_title']} - {a['section']}): \"{(a.get('claim') or '')[:180]}\"\n"
                )
                output += (
                    f"* Claim B ({b['paper_title']} - {b['section']}): \"{(b.get('claim') or '')[:180]}\"\n\n"
                )
                output += f"Conflict Type: {first['type']}\n"
                output += f"Strength: {first['strength']:.2f}\n\n"
                output += f"Explanation: {first.get('explanation','')[:320]}\n\n"
            else:
                output += f"{ConflictDetector.no_conflict_explanation(units)}\n\n"
                subq_data["conflicts"] = []

            comparison_text = self._build_grounded_comparison(sub_q, paper_profiles)

            output += "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n"
            output += "📊 Comparison Summary\n"
            output += f"{comparison_text}\n"
            subq_data["comparison_text"] = comparison_text

            output += "\n🧩 Final Synthesis\n"
            sub_conclusion = self._build_subquestion_conclusion(sub_q, units)
            output += f"{sub_conclusion}\n"
            subq_data["mini_summary"] = sub_conclusion
            analysis_data["sub_questions"].append(subq_data)
            
            output += "\n"
        
        # Summary section
        output += f"{'='*80}\n"
        output += f"SUMMARY\n"
        output += f"{'='*80}\n\n"
        output += f"Total Evidence Sources: {len(chunks)} chunks from {len(set(c.get('paper_id') for c in chunks))} papers\n"
        output += f"Sub-questions Addressed: {len(sub_questions)}\n"
        
        # List all unique papers
        unique_papers = {}
        for chunk in chunks:
            paper_id = chunk.get("paper_id")
            if paper_id not in unique_papers:
                unique_papers[paper_id] = {
                    "title": chunk.get("paper_title"),
                    "year": chunk.get("paper_year")
                }
        
        output += f"\n📚 Papers Referenced:\n"
        for pid, paper in unique_papers.items():
            output += f"   • {paper['title']} ({paper['year']})\n"

        refs: List[Dict[str, Any]] = []
        ref_by_paper: Dict[str, Dict[str, Any]] = {}
        for chunk in chunks:
            pid = chunk.get("paper_id", "")
            if not pid:
                continue
            doi = self._resolve_paper_doi(chunk)
            link = self._resolve_paper_link(chunk)
            if not link and pid:
                # Fallback stable link for traceability when DOI/link missing.
                link = f"https://openalex.org/{pid}" if str(pid).startswith("W") else ""
            ref_by_paper[pid] = {
                "paper_id": pid,
                "title": chunk.get("paper_title", "Unknown"),
                "year": str(chunk.get("paper_year", "N/A")),
                "doi": doi,
                "link": link,
            }
        refs = list(ref_by_paper.values())

        analysis_data["references"] = refs
        analysis_data["stats"] = {
            "chunks_used": len(chunks),
            "papers_used": len(refs),
            "sub_questions": len(sub_questions),
        }

        # ── Phase 3: Build comparison matrix & dataset overlap ────
        comparison_matrix = self._build_comparison_matrix(chunks, refs)
        dataset_overlap = self._build_dataset_overlap(comparison_matrix)
        analysis_data["paper_comparison_matrix"] = comparison_matrix
        analysis_data["cross_paper_dataset_overlap"] = dataset_overlap
        analysis_data["comparison_axes"] = plan.get("comparison_axes", [])
        print(
            f"✓ Comparison matrix: {len(comparison_matrix)} papers, "
            f"{len(dataset_overlap)} shared datasets"
        )

        self._last_analysis = analysis_data
        
        return output
    
