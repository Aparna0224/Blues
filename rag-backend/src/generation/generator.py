    def _build_comparison_matrix(
        self,
        chunks: List[Dict[str, Any]],
        references: List[Dict[str, Any]],
    ) -> List[Dict[str, Any]]:
        """Build a per-paper comparison row from aggregated paper_facts.

        Groups all chunks by paper_id, deduplicates extracted facts, and
        creates one structured row per paper suitable for deterministic
        comparison tables and LLM prompts.

        Returns:
            Sorted list of dicts (highest avg similarity first).
        """
        from collections import Counter as _Counter

        paper_map: Dict[str, Dict[str, Any]] = {}
        ref_lookup = {r.get("paper_id", ""): r for r in (references or [])}

        for c in chunks:
            pid = str(c.get("paper_id") or "")
            if not pid:
                continue
            if pid not in paper_map:
                ref = ref_lookup.get(pid, {})
                paper_map[pid] = {
                    "paper_id": pid,
                    "title": c.get("paper_title", ref.get("title", "Unknown")),
                    "year": str(c.get("paper_year", ref.get("year", "N/A"))),
                    "doi": ref.get("doi", ""),
                    "_datasets": set(),
                    "_model_names": set(),
                    "_method_kw_counter": _Counter(),
                    "_metrics": [],
                    "_sections": set(),
                    "_best_evidence": "",
                    "_best_score": 0.0,
                    "_sim_scores": [],
                }

            row = paper_map[pid]
            facts = c.get("paper_facts") or {}
            for ds in facts.get("datasets", []):
                row["_datasets"].add(ds)
            for mn in facts.get("model_names", []):
                row["_model_names"].add(mn)
            for mk in facts.get("method_keywords", []):
                row["_method_kw_counter"][mk] += 1
            for met in facts.get("metrics", []):
                if met not in row["_metrics"]:
                    row["_metrics"].append(met)

            section = str((c.get("metadata") or {}).get("section", c.get("section", "unknown")))
            row["_sections"].add(section)

            sim = float(c.get("similarity_score", 0.0) or 0.0)
            row["_sim_scores"].append(sim)

            ev = c.get("evidence_sentence") or c.get("text", "")
            ev_score = float(c.get("evidence_score", sim) or sim)
            if ev_score > row["_best_score"]:
                row["_best_score"] = ev_score
                row["_best_evidence"] = (ev or "")[:250]

        # Flatten into clean output
        matrix: List[Dict[str, Any]] = []
        for pid, row in paper_map.items():
            avg_sim = sum(row["_sim_scores"]) / max(1, len(row["_sim_scores"]))
            top_methods = [kw for kw, _ in row["_method_kw_counter"].most_common(5)]
            matrix.append({
                "paper_id": pid,
                "title": row["title"],
                "year": row["year"],
                "doi": row["doi"],
                "datasets": sorted(row["_datasets"]),
                "model_names": sorted(row["_model_names"]),
                "method_keywords": top_methods,
                "metrics": row["_metrics"][:10],
                "sections_covered": sorted(row["_sections"]),
                "top_evidence": row["_best_evidence"],
                "similarity_score": round(avg_sim, 4),
            })

        matrix.sort(key=lambda r: r["similarity_score"], reverse=True)
        return matrix

    @staticmethod
    def _build_dataset_overlap(
        matrix: List[Dict[str, Any]],
    ) -> Dict[str, List[str]]:
        """Find datasets used by two or more papers.

        Returns:
            Mapping of dataset name → list of paper titles that used it.
            Only datasets appearing in 2+ papers are included.
        """
        ds_to_papers: Dict[str, List[str]] = {}
        for row in matrix:
            title = row.get("title", "Unknown")
            for ds in row.get("datasets", []):
                ds_key = ds.strip()
                if not ds_key:
                    continue
                ds_to_papers.setdefault(ds_key, [])
                if title not in ds_to_papers[ds_key]:
                    ds_to_papers[ds_key].append(title)

        # Only return datasets shared by 2+ papers
        return {ds: papers for ds, papers in ds_to_papers.items() if len(papers) >= 2}

