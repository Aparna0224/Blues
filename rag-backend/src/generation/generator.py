    def _infer_section_from_content(self, text: str, labeled_section: str) -> str:
        """Infer TRUE section using explicit headers first, then strong cues."""
        if not text:
            return self._normalize_section(labeled_section)

        text_lower = text.lower()

        # Pre-check: trust labeled section if it's already meaningful
        normalized_label = self._normalize_section(labeled_section)
        if normalized_label not in {"unknown", "body"}:
            # Only override if very strong contradicting signal
            STRONG_RESULTS_SIGNALS = [
                "accuracy:", "f1:", "precision:", "recall:", "auc-roc",
                "mean average precision", "outperforms all baselines"
            ]
            if normalized_label != "Results" and any(
                s in text_lower for s in STRONG_RESULTS_SIGNALS
            ):
                return "Results"
            return normalized_label

        # Explicit override rules for section correction.
        if any(k in text_lower for k in ["in this paper", "we introduce"]):
            return "Introduction"
        if any(k in text_lower for k in ["we propose", "framework", "architecture"]):
            return "Methodology"
        if any(k in text_lower for k in ["results show", "evaluation"]):
            return "Results"
        if any(k in text_lower for k in ["limitation", "future work"]):
            return "Discussion"

        # Priority 1: explicit markdown/style headers
        if re.search(r"(^|\n)\s{0,3}(#\s*|##\s*|\d+\.?\s+)?introduction\b", text_lower):
            return "Introduction"
        if re.search(r"(^|\n)\s{0,3}(#\s*|##\s*|\d+\.?\s+)?(method|methods|methodology|approach|implementation)\b", text_lower):
            return "Methodology"
        if re.search(r"(^|\n)\s{0,3}(#\s*|##\s*|\d+\.?\s+)?(results|evaluation|experiments?|findings?)\b", text_lower):
            return "Results"
        if re.search(r"(^|\n)\s{0,3}(#\s*|##\s*|\d+\.?\s+)?(discussion|analysis|limitations?)\b", text_lower):
            return "Discussion"
        if re.search(r"(^|\n)\s{0,3}(#\s*|##\s*|\d+\.?\s+)?(conclusion|future work|summary)\b", text_lower):
            return "Conclusion"

        # Priority 2: strong results cues (more specific than weak methodology words)
        results_cues = [
            "accuracy", "precision", "recall", "f1", "benchmark", "outperform", "state-of-the-art",
            "experimental results", "evaluation results", "performance on"
        ]
        if any(cue in text_lower for cue in results_cues):
            return "Results"

        # Priority 3: strong introduction cues
        intro_cues = [
            "problem statement", "research question", "motivation", "this paper addresses", "background"
        ]
        if any(cue in text_lower for cue in intro_cues):
            return "Introduction"

        # Priority 4: strong methodology cues
        methodology_cues = [
            "algorithm", "pseudo-code", "training process", "hyperparameter", "implementation details",
            "network architecture", "loss function", "gradient"
        ]
        if any(cue in text_lower for cue in methodology_cues):
            return "Methodology"

        # Priority 5: trust labeled metadata section
        return self._normalize_section(labeled_section)

    def _resolve_true_section(self, chunk: Dict[str, Any]) -> tuple[str, bool]:
        """
        Resolve TRUE section using labeled + inferred.
        Returns (true_section, was_corrected).
        """
        labeled_section = self._normalize_section(
            chunk.get("metadata", {}).get("section") or chunk.get("section", "unknown")
        )
        text = chunk.get("text", "")
        inferred_section = self._infer_section_from_content(text, labeled_section)
        
        was_corrected = labeled_section.lower() != inferred_section.lower()
        return inferred_section, was_corrected

    def _is_methodology_subquestion(self, subq: str) -> bool:
        """Detect if sub-question is methodology-focused."""
        sq = (subq or "").lower()
        methodology_keywords = [
            "method", "approach", "how", "technique", "algorithm", "procedure",
            "framework", "pipeline", "system design", "architecture"
        ]
        return any(k in sq for k in methodology_keywords)

    def _section_allowed_for_subquestion(self, section: str, subq: str) -> bool:
        """Balanced section gating: always allow outcome sections."""
        if (section or "").lower() in {"results", "discussion", "conclusion"}:
            return True
        if not self._is_methodology_subquestion(subq):
            # Non-methodology sub-questions accept any section
            return True
        
        # Methodology sub-questions ONLY accept methodology-related sections
        section_lower = (section or "unknown").lower()
        return any(allowed in section_lower for allowed in self.METHODOLOGY_ALLOWED_SECTIONS)

    def _extract_heading_from_chunk(self, chunk: Dict[str, Any]) -> str:
        """Extract nearest heading from chunk metadata."""
        metadata = chunk.get("metadata") or {}
        heading = (
            metadata.get("heading") or
            metadata.get("subheading") or
            metadata.get("nearest_heading") or
            ""
        )
        return heading.strip()

    def _extract_important_points(self, chunk: Dict[str, Any]) -> Dict[str, Any]:
        """Extract 6 important fields from chunk."""
        text = chunk.get("text", "").lower()
        
        result = {
            "contributions": [],
            "dataset": [],
            "methodology": [],
            "models": [],
            "metrics": [],
            "limitations": []
        }
        
        # Extract contributions
        if any(w in text for w in ["propose", "introduce", "present", "novel", "contribution"]):
            result["contributions"].append(chunk.get("text", "")[:100])
        
        # Extract dataset references
        if any(w in text for w in ["dataset", "benchmark", "corpus", "data source", "collection"]):
            result["dataset"].append(chunk.get("text", "")[:100])
        
        # Extract methodology
        if any(w in text for w in ["method", "approach", "algorithm", "procedure", "pipeline"]):
            result["methodology"].append(chunk.get("text", "")[:100])
        
        # Extract model references
        if any(w in text for w in ["model", "network", "architecture", "framework"]):
            result["models"].append(chunk.get("text", "")[:100])
        
        # Extract metrics/performance
        if any(w in text for w in ["accuracy", "f1", "score", "metric", "performance", "evaluation"]):
            result["metrics"].append(chunk.get("text", "")[:100])
        
        # Extract limitations
        if any(w in text for w in ["limitation", "challenge", "future work", "gap", "drawback"]):
            result["limitations"].append(chunk.get("text", "")[:100])
        
        return result

    def _merge_paper_evidence(self, units: List[Dict[str, Any]]) -> Dict[str, Dict[str, Any]]:
        """
        Consolidate all evidence units by paper.
        Returns: {paper_id: {paper_id, title, sections, important_sections, traceable_citations}}
        """
        by_paper: Dict[str, Dict[str, Any]] = {}
        
        for unit in units:
            paper_id = unit.get("paper_id", "unknown")
            if paper_id not in by_paper:
                by_paper[paper_id] = {
                    "paper_id": paper_id,
                    "paper_title": unit.get("paper_title", "Unknown"),
                    "paper_year": unit.get("paper_year", "N/A"),
                    "sections": set(),
                    "important_sections": {},
                    "traceable_citations": [],
                }
            
            section = unit.get("section", "unknown")
            by_paper[paper_id]["sections"].add(section)
            
            # Track important points
            if "important_points" in unit:
                for field in self.IMPORTANT_FIELDS:
                    if field not in by_paper[paper_id]["important_sections"]:
                        by_paper[paper_id]["important_sections"][field] = []
                    points = unit.get("important_points", {}).get(field, [])
                    by_paper[paper_id]["important_sections"][field].extend(points)
            
            # Build traceable citation
            traceable = {
                "paper_title": unit.get("paper_title", "Unknown"),
                "section": section,
                "heading": unit.get("nearest_heading", ""),
                "location": f"sentences {unit.get('location_start', 1)}–{unit.get('location_end', 1)}",
                "text": unit.get("text", ""),
                "claim": unit.get("claim", "")
            }
            by_paper[paper_id]["traceable_citations"].append(traceable)
        
        # Convert sets to lists
        for paper_id in by_paper:
            by_paper[paper_id]["sections"] = list(by_paper[paper_id]["sections"])
        
        return by_paper

    def _quality_gate(self, merged_evidence: Dict[str, Dict[str, Any]]) -> Dict[str, Any]:
        """
        Pre-output validation.
        Returns: {status, issues, gaps}
        """
        issues = []
        gaps = []
        
        if not merged_evidence:
            return {
                "status": "rejected",
                "issues": ["No evidence units found"],
                "gaps": ["Complete absence of retrieved evidence"]
            }
        
        # Check section coverage (should span multiple sections)
        all_sections = set()
        for paper_data in merged_evidence.values():
            all_sections.update(paper_data.get("sections", []))
        
        if len(all_sections) < 2:
            gaps.append(f"Low section coverage: only {len(all_sections)} section(s) represented")
        
        # Check paper diversity
        num_papers = len(merged_evidence)
        if num_papers < 2:
            gaps.append(f"Low paper diversity: only {num_papers} paper(s)")
        
        # Check for missing traceability
        for paper_id, paper_data in merged_evidence.items():
            if not paper_data.get("traceable_citations"):
                issues.append(f"Paper {paper_id}: missing traceability")
        
        # Check for duplicate papers (shouldn't happen with merge, but check anyway)
        titles = [p.get("paper_title", "") for p in merged_evidence.values()]
        if len(titles) != len(set(titles)):
            issues.append("Duplicate papers detected in output")
        
        status = "accepted" if not issues and len(all_sections) >= 2 else "accepted_with_warnings"
        if issues:
            status = "rejected"
        
        return {
            "status": status,
            "issues": issues,
            "gaps": gaps if gaps else None,
            "section_coverage": list(all_sections),
            "paper_count": num_papers
        }

    @staticmethod
    def _lexical_similarity(a: str, b: str) -> float:
        ta = {
            t.strip(".,;:()[]{}\"'`").lower()
            for t in (a or "").split()
            if len(t.strip(".,;:()[]{}\"'`")) > 2
        }
        tb = {
            t.strip(".,;:()[]{}\"'`").lower()
            for t in (b or "").split()
            if len(t.strip(".,;:()[]{}\"'`")) > 2
        }
        if not ta or not tb:
            return 0.0
        inter = len(ta.intersection(tb))
        union = len(ta.union(tb))
        return inter / union if union else 0.0

    @staticmethod
    def _keyword_overlap(a: str, b: str) -> float:
        """Normalized keyword overlap for soft scoring."""
        ta = {
            t.strip(".,;:()[]{}\"'`").lower()
            for t in (a or "").split()
            if len(t.strip(".,;:()[]{}\"'`")) > 2
        }
        tb = {
            t.strip(".,;:()[]{}\"'`").lower()
            for t in (b or "").split()
            if len(t.strip(".,;:()[]{}\"'`")) > 2
        }
        if not ta or not tb:
            return 0.0
        return len(ta.intersection(tb)) / max(1, min(len(ta), len(tb)))

    def _section_weight(self, sub_question: str, section: str) -> float:
        sq = (sub_question or "").lower()
        sec = (section or "unknown").lower()
        if any(k in sq for k in ["method", "approach", "how"]):
            return 1.20 if "method" in sec else 1.0
        if any(k in sq for k in ["result", "performance", "accuracy", "effective"]):
            return 1.20 if "result" in sec else 1.0
        if any(k in sq for k in ["challenge", "limitation", "issue", "risk"]):
            return 1.20 if ("discussion" in sec or "conclusion" in sec) else 1.0
        return 1.0

    def _infer_intent(self, sub_question: str, provided_intent: str = "") -> str:
        intent = (provided_intent or "").strip().lower()
        if intent in self.INTENT_KEYWORDS:
            return intent
        sq = (sub_question or "").lower()
        for k, words in self.INTENT_KEYWORDS.items():
            if any(w in sq for w in words):
                return k
        return "methodology"

    def _chunk_matches_intent(self, chunk_text: str, intent: str) -> bool:
        if not chunk_text:
            return False
        text = chunk_text.lower()
        keywords = self.INTENT_KEYWORDS.get(intent, [])
        if not keywords:
            return True

        # Require explicit dataset grounding for dataset-focused sub-questions.
        if intent == "dataset":
            has_dataset_keyword = any(w in text for w in keywords)
            has_only_detection = any(w in text for w in self.INTENT_KEYWORDS.get("detection", [])) and not has_dataset_keyword
            return has_dataset_keyword and not has_only_detection

        return any(w in text for w in keywords)

    @staticmethod
    def _is_dataset_subquestion(sub_question: str) -> bool:
        sq = (sub_question or "").lower()
        return any(k in sq for k in ["dataset", "data", "training"])

    def _dataset_signal_score(self, chunk_text: str) -> float:
        text = (chunk_text or "").lower()
        if not text:
            return 0.0
        hits = sum(1 for s in self.DATASET_SIGNALS if s in text)
        return min(1.0, hits / 3.0)

    def _is_background_drift(self, sub_question: str, chunk_text: str, user_level: str) -> bool:
        level = (user_level or "auto").lower()
        if level not in {"intermediate", "advanced"}:
            return False

        sq = (sub_question or "").lower()
        if any(k in sq for k in ["overview", "introduction", "basic", "what is", "iot"]):
            return False

        text = (chunk_text or "").lower()
        return any(term in text for term in self.BACKGROUND_DRIFT_TERMS)

    @staticmethod
    def _simplify_text(text: str) -> str:
        if not text:
            return ""
        s = text.lower()
        s = re.sub(r"[^a-z0-9\s]", " ", s)
        s = re.sub(r"\s+", " ", s).strip()
        return s

    def _expand_query_forms(self, sub_question: str) -> List[str]:
        """Build expanded query forms (original + synonym-expanded + simplified)."""
        base = (sub_question or "").strip()
        if not base:
            return []

        synonyms = {
            "machine learning": ["deep learning", "ml models"],
            "deep learning": ["neural network", "cnn", "model"],
            "performance": ["accuracy", "results", "evaluation"],
            "method": ["approach", "technique", "pipeline"],
            "challenges": ["limitations", "issues", "difficulties"],
            "segmentation": ["partition", "delineation", "extraction"],
        }

        expanded = [base]
        expanded_text = base
        lower = base.lower()
        for key, vals in synonyms.items():
            if key in lower:
                expanded_text += " " + " ".join(vals)
        expanded.append(expanded_text)
        expanded.append(self._simplify_text(base))

        # Keep unique, non-empty forms
        unique: List[str] = []
        seen = set()
        for q in expanded:
            nq = q.strip()
            if not nq:
                continue
            if nq not in seen:
                seen.add(nq)
                unique.append(nq)
        return unique

    def _best_scored_contiguous_window(
        self,
        sentences: List[str],
        scores: List[float],
        min_s: int,
        max_s: int,
    ) -> Dict[str, Any]:
        if not sentences:
            return {"sentences": [], "start": 0, "end": 0, "score": 0.0}

        best = {
            "sentences": sentences[:min_s],
            "start": 0,
            "end": min(len(sentences), min_s),
            "score": -1.0,
        }
        max_len = min(max_s, len(sentences))
        for win_len in range(min_s, max_len + 1):
            for start in range(0, len(sentences) - win_len + 1):
                end = start + win_len
                window = sentences[start:end]
                window_scores = scores[start:end]
                coherence_bonus = 0.1 if self._is_coherent_window(window) else 0.0
                avg_score = (sum(window_scores) / max(1, len(window_scores))) + coherence_bonus
                if avg_score > best["score"]:
                    best = {
                        "sentences": window,
                        "start": start,
                        "end": end,
                        "score": avg_score,
                    }
        return best

    def _build_evidence_paragraph(
        self,
        sub_question: str,
        text: str,
        evidence_extractor,
    ) -> Dict[str, Any]:
        if evidence_extractor and hasattr(evidence_extractor, "select_best_paragraph"):
            selected = evidence_extractor.select_best_paragraph(
                query=sub_question,
                text=text,
                min_similarity=0.0,
                min_sentences=self.PARAGRAPH_MIN_SENTENCES,
                max_sentences=self.PARAGRAPH_MAX_SENTENCES,
            )
            paragraph = (selected.get("best_paragraph") or "").strip()
            if paragraph:
                all_scored = selected.get("all_sentences") or []
                best_score = float(selected.get("best_score", 0.0) or 0.0)
                best_sentence = ""
                if all_scored:
                    best_sentence = max(all_scored, key=lambda x: float(x[1] or 0.0))[0]
                return {
                    "paragraph": paragraph,
                    "start": int(selected.get("start", 1) or 1),
                    "end": int(selected.get("end", 1) or 1),
                    "best_sentence": best_sentence,
                    "best_score": best_score,
                }

        sentences = [self._clean_sentence(s) for s in self._split_sentences(text)]
        sentences = [s for s in sentences if s and not self._is_noise_sentence(s)]
        if not sentences:
            return {
                "paragraph": "",
                "start": 1,
                "end": 1,
                "best_sentence": "",
                "best_score": 0.0,
            }

        scores: List[float] = []
        for s in sentences:
            scores.append(self._lexical_similarity(sub_question, s))

        ranked_ids = sorted(range(len(sentences)), key=lambda i: scores[i], reverse=True)
        top_ids = ranked_ids[: min(len(ranked_ids), self.PARAGRAPH_MAX_SENTENCES)]
        coherent_ids: List[int] = []
        for idx in top_ids:
            if len(coherent_ids) >= self.PARAGRAPH_MAX_SENTENCES:
                break
            candidate = coherent_ids + [idx]
            candidate_sents = [sentences[i] for i in sorted(candidate)]
            if len(candidate_sents) <= 1 or self._is_coherent_window(candidate_sents):
                coherent_ids.append(idx)

        coherent_ids = sorted(coherent_ids)
        contiguous = bool(coherent_ids) and (coherent_ids[-1] - coherent_ids[0] + 1 == len(coherent_ids))
        coherent_ok = len(coherent_ids) >= self.PARAGRAPH_MIN_SENTENCES and self._is_coherent_window([sentences[i] for i in coherent_ids])

        if contiguous or coherent_ok:
            selected = coherent_ids
            if len(selected) < self.PARAGRAPH_MIN_SENTENCES:
                window = self._best_scored_contiguous_window(
                    sentences,
                    scores,
                    self.PARAGRAPH_MIN_SENTENCES,
                    self.PARAGRAPH_MAX_SENTENCES,
                )
                selected = list(range(window["start"], window["end"]))
        else:
            window = self._best_scored_contiguous_window(
                sentences,
                scores,
                self.PARAGRAPH_MIN_SENTENCES,
                self.PARAGRAPH_MAX_SENTENCES,
            )
            selected = list(range(window["start"], window["end"]))

        if not selected:
            return {
                "paragraph": "",
                "start": 1,
                "end": 1,
                "best_sentence": "",
                "best_score": 0.0,
            }

        paragraph = " ".join(sentences[i] for i in selected).strip()
        best_idx = max(selected, key=lambda i: scores[i])
        return {
            "paragraph": paragraph,
            "start": selected[0] + 1,
            "end": selected[-1] + 1,
            "best_sentence": sentences[best_idx],
            "best_score": scores[best_idx],
        }

    def _build_subquestion_conclusion(self, sub_question: str, units: List[Dict[str, Any]]) -> str:
        if not units:
            return "Insufficient evidence to draw a conclusion for this sub-question."

        paper_names = list({u.get("paper_title", "Unknown")[:35] for u in units})
        sections = list({u.get("section", "unknown") for u in units})
        avg_conf = sum(float(u.get("confidence", 0)) for u in units) / max(1, len(units))

        return (
            f"Evidence drawn from {len(paper_names)} paper(s) "
            f"({'; '.join(paper_names[:3])}{'...' if len(paper_names) > 3 else ''}) "
            f"across sections: {', '.join(sections)}. "
            f"Average evidence confidence: {avg_conf:.2f}. "
            f"See the AI Research Summary section for synthesized interpretation."
        )

    def _extract_signal_sentences(self, text: str, keywords: List[str], max_items: int = 2) -> List[str]:
        if not text:
            return []
        sents = [self._clean_sentence(s) for s in self._split_sentences(text)]
        sents = [s for s in sents if s and not self._is_noise_sentence(s)]
        hits: List[str] = []
        for s in sents:
            low = s.lower()
            if any(k in low for k in keywords):
                hits.append(s)
                if len(hits) >= max_items:
                    break
        return hits

    def _build_paper_profiles(self, chunks: List[Dict[str, Any]]) -> Dict[str, Dict[str, Any]]:
        """Create paper-level structured facets for downstream reasoning."""
        profiles: Dict[str, Dict[str, Any]] = {}
        for c in chunks:
            pid = str(c.get("paper_id") or "")
            if not pid:
                continue
            if pid not in profiles:
                profiles[pid] = {
                    "paper_id": pid,
                    "paper_title": c.get("paper_title", "Unknown"),
                    "paper_year": c.get("paper_year", "N/A"),
                    "dataset": [],
                    "method": [],
                    "results": [],
                    "application": [],
                    "chunks": [],
                }

            text = c.get("text", "")
            profiles[pid]["dataset"].extend(
                self._extract_signal_sentences(
                    text,
                    ["dataset", "benchmark", "corpus", "training data", "data collection"],
                )
            )
            profiles[pid]["method"].extend(
                self._extract_signal_sentences(
                    text,
                    ["method", "approach", "framework", "pipeline", "algorithm", "architecture"],
                )
            )
            profiles[pid]["results"].extend(
                self._extract_signal_sentences(
                    text,
                    ["result", "accuracy", "f1", "precision", "recall", "outperform", "evaluation"],
                )
            )
            profiles[pid]["application"].extend(
                self._extract_signal_sentences(
                    text,
                    ["application", "use case", "deployment", "real-world", "practical", "scenario"],
                )
            )
            profiles[pid]["chunks"].append(c)

        for p in profiles.values():
            for facet in ["dataset", "method", "results", "application"]:
                seen = set()
                unique = []
                for v in p[facet]:
                    key = v.strip().lower()
                    if key and key not in seen:
                        seen.add(key)
                        unique.append(v)
                p[facet] = unique[:4]
        return profiles

    def _build_grounded_comparison(self, sub_question: str, paper_profiles: Dict[str, Dict[str, Any]]) -> str:
        if not paper_profiles:
            return "Insufficient evidence for cross-paper comparison."

        lines = []
        profiles = list(paper_profiles.values())[:5]

        # Per-paper method statement (cite actual extracted sentences)
        method_lines = []
        for p in profiles:
            name = f"{p.get('paper_title', 'Unknown')[:40]} ({p.get('paper_year', 'N/A')})"
            methods = p.get("method", [])
            if methods:
                method_lines.append(f"  • {name}: {methods[0][:120]}")

        # Per-paper result statement
        result_lines = []
        for p in profiles:
            name = f"{p.get('paper_title', 'Unknown')[:40]} ({p.get('paper_year', 'N/A')})"
            results = p.get("results", [])
            if results:
                result_lines.append(f"  • {name}: {results[0][:120]}")

        # Agreement or disagreement statement
        all_units = [
            u for p in profiles for u in p.get("chunks", [])
        ]
        conflicts = ConflictDetector.detect_conflicts(all_units)

        if method_lines:
            lines.append(f"Method approaches across {len(profiles)} paper(s):")
            lines.extend(method_lines)
        if result_lines:
            lines.append(f"\nReported outcomes:")
            lines.extend(result_lines)
        if conflicts:
            c = conflicts[0]
            lines.append(
                f"\nConflict: {c.get('a', {}).get('paper_title', 'Paper A')[:30]} vs "
                f"{c.get('b', {}).get('paper_title', 'Paper B')[:30]} on overlapping claims "
                f"(type: {c.get('type', 'unknown')}, strength: {c.get('strength', 0):.2f})"
            )
        else:
            lines.append(
                f"\nConsensus: The {len(profiles)} paper(s) are directionally "
                f"aligned with no strongly incompatible claims detected."
            )

        return "\n".join(lines) if lines else "No comparison data available."

    def _build_evidence_unit(
        self,
        sub_question: str,
        chunk: Dict[str, Any],
        evidence_extractor,
        subquery_similarity: float,
    ) -> Dict[str, Any] | None:
        text = chunk.get("text", "")
        if not text:
            return None

        evidence_sentence = chunk.get("evidence_sentence", "")
        evidence_score = float(chunk.get("evidence_score", 0.0) or 0.0)
        similarity_score = float(chunk.get("similarity_score", 0.0) or 0.0)
        paragraph_info = self._build_evidence_paragraph(sub_question, text, evidence_extractor)
        snippet = paragraph_info.get("paragraph", "")
        sentence_start = int(paragraph_info.get("start", 1))
        sentence_end = int(paragraph_info.get("end", 1))
        if not snippet:
            return None

        evidence_sentence = paragraph_info.get("best_sentence", evidence_sentence)
        evidence_score = max(evidence_score, float(paragraph_info.get("best_score", 0.0) or 0.0))

        verification_score = float(
            chunk.get("verification_score", chunk.get("confidence_score", similarity_score)) or similarity_score
        )

        metadata = chunk.get("metadata") or {}
        labeled_section = self._normalize_section(metadata.get("section") or chunk.get("section", "unknown"))
        
        # STEP 3: Resolve TRUE section with correction tracking
        true_section, section_corrected = self._resolve_true_section(chunk)
        
        # STEP 3: Soft section gating (penalty, not hard-drop)
        section_penalty = 0.0
        if not self._section_allowed_for_subquestion(true_section, sub_question):
            section_penalty = 0.12
        
        section_weight = self._section_weight(sub_question, true_section)

        confidence = max(
            0.0,
            min(
                1.0,
                (self.CONFIDENCE_W1 * subquery_similarity)
                + (self.CONFIDENCE_W2 * evidence_score)
                + (self.CONFIDENCE_W3 * verification_score),
            ),
        )
        keyword_overlap = float(
            chunk.get("keyword_overlap", self._keyword_overlap(sub_question, snippet)) or 0.0
        )
        base_score = (
            (0.4 * float(chunk.get("rrf_score", 0.0) or 0.0))
            + (0.3 * subquery_similarity)
            + (0.2 * evidence_score)
            + (0.1 * keyword_overlap)
        )
        similarity_penalty = 0.0
        if subquery_similarity < self.SUBQUERY_HARD_GATE_THRESHOLD:
            similarity_penalty = (self.SUBQUERY_HARD_GATE_THRESHOLD - subquery_similarity) * 0.25

        final_score = max(
            0.0,
            min(
                1.0,
                base_score - similarity_penalty - section_penalty,
            ),
        )

        # Extract traceability metadata
        nearest_heading = self._extract_heading_from_chunk(chunk)
        important_points = self._extract_important_points(chunk)

        return {
            "chunk_id": chunk.get("chunk_id", ""),
            "paper_id": chunk.get("paper_id", ""),
            "paper_title": chunk.get("paper_title", "Unknown"),
            "paper_year": chunk.get("paper_year", "N/A"),
            "paper_doi": self._resolve_paper_doi(chunk),
            "paper_link": self._resolve_paper_link(chunk),
            "section": true_section,
            "section_corrected": section_corrected,
            "labeled_section": labeled_section,
            "nearest_heading": nearest_heading,
            "location_start": sentence_start,
            "location_end": sentence_end,
            "relevance": similarity_score,
            "evidence_score": evidence_score,
            "verification_score": verification_score,
            "final_score": final_score,
            "subquery_similarity": subquery_similarity,
            "confidence": confidence,
            "confidence_band": self._confidence_band(confidence),
            "text": snippet,
            "claim": evidence_sentence or snippet,
            "important_points": important_points,
        }
    
    def generate_answer(self, query: str, retrieved_chunks: List[Dict[str, Any]]) -> str:
        """
        Generate an answer from retrieved chunks.
        
        For MVP: concatenate chunks into structured answer with citations.
        
        Args:
            query: Original user query
            retrieved_chunks: List of retrieved chunk objects
            
        Returns:
            Formatted answer with citations
        """
        if not retrieved_chunks:
            return "I could not find relevant information to answer your question."
        
        try:
            answer = self._build_structured_answer(query, retrieved_chunks)
            return answer
        except Exception as e:
            print(f"✗ Error generating answer: {e}")
            return "Error generating answer from retrieved chunks."
    
    def _build_structured_answer(self, query: str, chunks: List[Dict[str, Any]]) -> str:
        """
        Build structured answer from chunks with citations.
        
        Args:
            query: User query
            chunks: Retrieved chunks
            
        Returns:
            Formatted answer
        """
        answer = f"\nQuestion: {query}\n\n"
        answer += "="*80 + "\n"
        answer += "ANSWER\n"
        answer += "="*80 + "\n\n"
        
        # Collect evidence by paper
        evidence_by_paper = {}
        for chunk in chunks:
            paper_key = f"{chunk['paper_title']} ({chunk['paper_year']})"
            if paper_key not in evidence_by_paper:
                evidence_by_paper[paper_key] = []
            evidence_by_paper[paper_key].append(chunk)
        
        # Build answer with citations
        answer_text = "Based on the retrieved research, "
        
        # Add first chunk as main evidence
        if chunks:
            first_chunk = chunks[0]
            answer_text += f"{first_chunk['text']} "
            answer_text += f"[{first_chunk['paper_title']}, {first_chunk['paper_year']}]\n\n"
        
        answer += answer_text
        answer += self._format_citations(evidence_by_paper)
        
        return answer
    
    def _format_citations(self, evidence_by_paper: Dict[str, List[Dict[str, Any]]]) -> str:
        """
        Format citations section.
        
        Args:
            evidence_by_paper: Dictionary of evidence grouped by paper
            
        Returns:
            Formatted citations
        """
        citations = "="*80 + "\n"
        citations += "EVIDENCE CITATIONS\n"
        citations += "="*80 + "\n\n"
        
        for paper_key, chunks in evidence_by_paper.items():
            citations += f"📄 {paper_key}\n"
            scores = ", ".join([f"{c.get('similarity_score', 0):.4f}" for c in chunks])
            citations += f"   Relevance Scores: {scores}\n"
            citations += f"   Evidence:\n"
            for i, chunk in enumerate(chunks, 1):
                chunk_text = chunk.get('text', '')[:80]
                citations += f"     {i}. {chunk_text}...\n"
            citations += "\n"
        
        return citations
    
